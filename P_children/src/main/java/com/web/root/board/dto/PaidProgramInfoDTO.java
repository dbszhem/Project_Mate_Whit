package com.web.root.board.dto;

public class PaidProgramInfoDTO {
	private String id;
	private String title;
	private int write_no;
	private int num;
	private String cancel_request;
	private String tid;
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public int getWrite_no() {
		return write_no;
	}
	public void setWrite_no(int write_no) {
		this.write_no = write_no;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getCancel_request() {
		return cancel_request;
	}
	public void setCancel_request(String cancel_request) {
		this.cancel_request = cancel_request;
	}
	public String getTid() {
		return tid;
	}
	public void setTid(String tid) {
		this.tid = tid;
	}
	
	
	
	
}
